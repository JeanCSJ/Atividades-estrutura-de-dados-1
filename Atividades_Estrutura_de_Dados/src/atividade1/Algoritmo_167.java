package atividade1;

public class Algoritmo_167 {

	public static void main(String[] args) {
		int L, c, t
		;

		for (L = 1; L <= 9; L++) {
			for (c = L + 1; c <= 10; c++) {
				System.out.print("\t" + L + "-" + c);
				}
			System.out.println();

			for (t = 1; t <= L; t++) {
				System.out.print("\t");
				}
			};

		System.out.println("\n");
	}

}
